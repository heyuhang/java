package com.hyh.vo;
/*
 * 部门 vo
 */
public class section {

	private int id;
	private String sename;
	private String seleader;
	private String sedescription;
	private int state;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSename() {
		return sename;
	}
	public void setSename(String sename) {
		this.sename = sename;
	}
	public String getSeleader() {
		return seleader;
	}
	public void setSeleader(String seleader) {
		this.seleader = seleader;
	}
	public String getSedescription() {
		return sedescription;
	}
	public void setSedescription(String sedescription) {
		this.sedescription = sedescription;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
	
}
