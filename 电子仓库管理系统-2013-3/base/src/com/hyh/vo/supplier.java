package com.hyh.vo;
/*
 * 供应商  vo
 */
public class supplier {

	private int id;
	private String num;
	private String suname;
	private String sucity;
	private String address;
	private String spost;
	private String sphone;
	private String slpeople;
	private int state;

	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSuname() {
		return suname;
	}
	public void setSuname(String suname) {
		this.suname = suname;
	}
	public String getSucity() {
		return sucity;
	}
	public void setSucity(String sucity) {
		this.sucity = sucity;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getSpost() {
		return spost;
	}
	public void setSpost(String spost) {
		this.spost = spost;
	}
	public String getSphone() {
		return sphone;
	}
	public void setSphone(String sphone) {
		this.sphone = sphone;
	}
	public String getSlpeople() {
		return slpeople;
	}
	public void setSlpeople(String slpeople) {
		this.slpeople = slpeople;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	
	
}
